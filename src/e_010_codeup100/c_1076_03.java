public class c_1076_03 {

    public static void main(String[] args) throws Exception {

        Scanner sc = new Scanner(System.in);

        char input = sc.nextLine().charAt(0);

        char c = 'a';

        do {
            System.out.println(c++ + "");
        } while (c <= input);

        sc.close();
    }
}
